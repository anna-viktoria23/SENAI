#Método sort() pode ser utilizado para ordenar as listas de valores numéricos ou de strings.

#Exemplo 16: Ordenação de lista. (não quer dizer que vai mudar a posição para
# sempre, saindo do comando ela volta ao normal)

# -*- coding: UTF-8 -*-

numeros = [2, 5, 3.14, 1, -7]

numeros.sort()

print (numeros)

animais = ["macacos", "gatos", "cachorros", "ursos",
"elefantes"]

animais.sort()

print (animais)
